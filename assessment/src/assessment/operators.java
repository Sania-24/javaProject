package assessment;

public class operators {

	public static void main(String[] args) {
    int i,j,k,l,m,n;
    i=10;
    j=5;
    k=i+j;
    l=i-j;
    m=i*j;
    n=i/j;
    System.out.println("k="+k+" "+"l="+l+" "+"m="+m+" "+"n="+n);
    for(i=0;i<=10;i++)//increment operator
    {
    	System.out.print(i+" ");
    	
    }
    System.out.println();
    for(i=10;i>=0;i--)
    	System.out.print(i+" ");

	}

}
